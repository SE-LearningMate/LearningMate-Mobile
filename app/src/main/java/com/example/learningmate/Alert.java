package com.example.learningmate;

public class Alert {
    public String alertName;
    public int year;
    public int month;
    public int day;
    public String state;

    public Alert(String alertName, int year, int month, int day, String state){
        this.alertName = alertName;
        this.year = year;
        this.month = month;
        this.day = day;
        this.state = state;
    }
}