package com.example.learningmate;

public class Homework {
    public String homeworkName;
    public int year;
    public int month;
    public int day;
    public int score;

    public Homework(String homeworkName, int year, int month, int day, int score){
        this.homeworkName = homeworkName;
        this.year = year;
        this.month = month;
        this.day = day;
        this.score = score;
    }
}
